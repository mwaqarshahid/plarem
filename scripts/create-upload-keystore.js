#!/usr/bin/env node
/**
 * Creates the Play Store upload keystore + android/keystore.properties (gitignored).
 * Does nothing if the keystore already exists (pass --force to recreate — only if
 * you have NOT yet uploaded a build signed with the current upload key).
 */
const { spawnSync } = require('child_process');
const crypto = require('crypto');
const fs = require('fs');
const path = require('path');

const force = process.argv.includes('--force');
const root = path.join(__dirname, '..');
const androidDir = path.join(root, 'android');
// Path relative to android/app (Gradle `file(...)` resolves from the app module).
const keystoreFileName = 'plarem-upload.keystore';
const keystorePath = path.join(androidDir, 'app', keystoreFileName);
const propsPath = path.join(androidDir, 'keystore.properties');
const alias = 'plarem-upload';

const keytoolCandidates = [
  process.env.JAVA_HOME && path.join(process.env.JAVA_HOME, 'bin', 'keytool'),
  'C:\\Program Files\\Java\\jdk-17\\bin\\keytool.exe',
  'C:\\Program Files\\Android\\Android Studio\\jbr\\bin\\keytool.exe',
  'keytool',
].filter(Boolean);

function findKeytool() {
  for (const candidate of keytoolCandidates) {
    if (candidate === 'keytool') {
      const probe = spawnSync(candidate, ['-help'], { encoding: 'utf8' });
      if (probe.status === 0 || probe.status === 1) {
        return candidate;
      }
      continue;
    }
    if (fs.existsSync(candidate)) {
      return candidate;
    }
  }
  return null;
}

if (fs.existsSync(keystorePath) && !force) {
  console.log(`Keystore already exists: ${path.relative(root, keystorePath)}`);
  console.log('Pass --force only if you intentionally need a new upload key (breaks Play updates if already published).');
  process.exit(0);
}

const keytool = findKeytool();
if (!keytool) {
  console.error('keytool not found. Install JDK 17+ and ensure JAVA_HOME is set.');
  process.exit(1);
}

const password = crypto.randomBytes(24).toString('base64url');
const dname = 'CN=Plarem, OU=Mobile, O=Plarem, L=Lahore, ST=Punjab, C=PK';

fs.mkdirSync(path.dirname(keystorePath), { recursive: true });
if (fs.existsSync(keystorePath)) {
  fs.unlinkSync(keystorePath);
}

const args = [
  '-genkeypair',
  '-v',
  '-storetype',
  'PKCS12',
  '-keystore',
  keystorePath,
  '-alias',
  alias,
  '-keyalg',
  'RSA',
  '-keysize',
  '2048',
  '-validity',
  '10000',
  '-storepass',
  password,
  '-keypass',
  password,
  '-dname',
  dname,
];

const result = spawnSync(keytool, args, { stdio: 'inherit' });
if (result.status !== 0) {
  process.exit(result.status ?? 1);
}

const props = [
  '# Generated by scripts/create-upload-keystore.js — DO NOT COMMIT',
  `storeFile=${keystoreFileName}`,
  `storePassword=${password}`,
  `keyAlias=${alias}`,
  `keyPassword=${password}`,
  '',
].join('\n');

fs.writeFileSync(propsPath, props, { encoding: 'utf8', mode: 0o600 });

const fingerprint = spawnSync(
  keytool,
  ['-list', '-v', '-keystore', keystorePath, '-alias', alias, '-storepass', password],
  { encoding: 'utf8' },
);

const sha1 = fingerprint.stdout?.match(/SHA1:\s*([0-9A-Fa-f:]+)/)?.[1] ?? '(see keytool -list -v)';
const sha256 = fingerprint.stdout?.match(/SHA256:\s*([0-9A-Fa-f:]+)/)?.[1] ?? '';

console.log('');
console.log('Upload keystore created.');
console.log(`  Keystore: ${path.relative(root, keystorePath)}`);
console.log(`  Properties: ${path.relative(root, propsPath)} (gitignored)`);
console.log(`  Alias: ${alias}`);
console.log(`  SHA-1: ${sha1}`);
if (sha256) {
  console.log(`  SHA-256: ${sha256}`);
}
console.log('');
console.log('IMPORTANT: Back up the keystore file AND keystore.properties (password manager + offline copy).');
console.log('Losing them makes Play Store updates difficult.');
console.log('Add this SHA-1 to your Google Maps Android API key restriction (package com.plarem).');
